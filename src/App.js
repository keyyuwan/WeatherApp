import React, { useState } from "react";
import Location from "./Location";
import Weather from "./Weather";
import Search from "./Search";

const api = {
  key: "ef7391e9bf4b51d032fe33def04cb9aa",
  base: "https://api.openweathermap.org/data/2.5/",
};

function App() {
  const [query, setQuery] = useState("");
  const [weather, setWeather] = useState({});

  const search = (event) => {
    if (event.key === "Enter") {
      fetch(`${api.base}weather?q=${query}&units=metric&APPID=${api.key}`)
        .then((res) => res.json())
        .then((result) => {
          setWeather(result);
          setQuery("");
        });
    }
  };

  const dateBuilder = (d) => {
    let months = [
      "January",
      "February",
      "March",
      "April",
      "May",
      "June",
      "July",
      "August",
      "September",
      "October",
      "November",
      "December",
    ];
    let days = [
      "Sunday",
      "Monday",
      "Tuesday",
      "Wednesday",
      "Thursday",
      "Friday",
      "Saturday",
    ];

    let day = days[d.getDay()];
    let date = d.getDate();
    let month = months[d.getMonth()];
    let year = d.getFullYear();

    return `${day} ${date} ${month} ${year}`;
  };

  return (
    <div
      className={
        typeof weather.main != "undefined"
          ? weather.main.temp > 16
            ? "app warm"
            : "app"
          : "app"
      }
    >
      <main>
        <Search search={search} query={query} setQuery={setQuery}></Search>
        {typeof weather.main != "undefined" ? (
          <div>
            <Location weather={weather} dateBuilder={dateBuilder}></Location>
            <Weather weather={weather}></Weather>
          </div>
        ) : (
          ""
        )}
      </main>
    </div>
  );
}

export default App;
